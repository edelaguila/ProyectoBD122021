    private void cargarTabla(PartidaContable pc) {

        PartidaContable objPartida = new PartidaContable();

        objPartida.getTablaPartidas(pc);

        int contI = 0;
        contI = objPartida.getTablaPartidas(pc).length;

        String[][] matrixData = new String[contI][3];

        for (int i = 0; i < contI; i++) {
            for (int j = 0; j < 3; j++) {
                matrixData[i][j] = objPartida.getTablaPartidas(pc)[i][j];
            }
        }

        DefaultTableModel modeloTabla = (DefaultTableModel) TblPartida.getModel();
        modeloTabla.setRowCount(contI);
        modeloTabla.setColumnCount(3);

        for (int i = 0; i < contI; i++) {
            for (int j = 0; j < 3; j++) {
                TblPartida.setValueAt(matrixData[i][j], i, j);;
            }
        }
    }

