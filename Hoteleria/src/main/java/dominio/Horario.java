package dominio;

/**
 *
 * @author JEFF
 */
public class Horario {

    private String idHorario, horasExtrasHorario, entradaHorario, salidaHorario, descripcionHorario, estadoHorario;

    public String getIdHorario() {
        return idHorario;
    }

    public void setIdHorario(String idHorario) {
        this.idHorario = idHorario;
    }

    public String getHorasExtrasHorario() {
        return horasExtrasHorario;
    }

    public void setHorasExtrasHorario(String horasExtrasHorario) {
        this.horasExtrasHorario = horasExtrasHorario;
    }

    public String getEntradaHorario() {
        return entradaHorario;
    }

    public void setEntradaHorario(String entradaHorario) {
        this.entradaHorario = entradaHorario;
    }

    public String getSalidaHorario() {
        return salidaHorario;
    }

    public void setSalidaHorario(String salidaHorario) {
        this.salidaHorario = salidaHorario;
    }

    public String getDescripcionHorario() {
        return descripcionHorario;
    }

    public void setDescripcionHorario(String descripcionHorario) {
        this.descripcionHorario = descripcionHorario;
    }

    public String getEstadoHorario() {
        return estadoHorario;
    }

    public void setEstadoHorario(String estadoHorario) {
        this.estadoHorario = estadoHorario;
    }
}
