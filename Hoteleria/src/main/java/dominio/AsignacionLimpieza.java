public class AsignacionLimpieza {
    private String idAsignacionLimpieza, idAsignacionGobernanta, idPiso, idHorario, estadoAsignacionLimpieza;

    public String getIdAsignacionLimpieza() {
        return idAsignacionLimpieza;
    }

    public void setIdAsignacionLimpieza(String idAsignacionLimpieza) {
        this.idAsignacionLimpieza = idAsignacionLimpieza;
    }

    public String getIdAsignacionGobernanta() {
        return idAsignacionGobernanta;
    }

    public void setIdAsignacionGobernanta(String idAsignacionGobernanta) {
        this.idAsignacionGobernanta = idAsignacionGobernanta;
    }

    public String getIdPiso() {
        return idPiso;
    }

    public void setIdPiso(String idPiso) {
        this.idPiso = idPiso;
    }

    public String getIdHorario() {
        return idHorario;
    }

    public void setIdHorario(String idHorario) {
        this.idHorario = idHorario;
    }

    public String getEstadoAsignacionLimpieza() {
        return estadoAsignacionLimpieza;
    }

    public void setEstadoAsignacionLimpieza(String estadoAsignacionLimpieza) {
        this.estadoAsignacionLimpieza = estadoAsignacionLimpieza;
    }
}
