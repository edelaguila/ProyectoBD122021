    private void btn_iniciar_sesionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_iniciar_sesionMouseClicked
        if (prcs_repetidos.isNoneEmpty(txt_usuario, txt_password, txt_area)) {
            
                seguridad.dominio.Usuario usuarioAConsultar = new seguridad.dominio.Usuario();
                UsuarioDAO usuarioDAO = new UsuarioDAO();
                usuarioAConsultar.setUser_usuario((txt_usuario.getText()));
                usuarioAConsultar = usuarioDAO.query(usuarioAConsultar);
                 String nuevoPass = Hash.sha1(txt_password.getText());
                
                if (nuevoPass.equals(usuarioAConsultar.getPassword_usuario()) && txt_usuario.getText().equals(usuarioAConsultar.getUser_usuario())) {
                    switch (txt_area.getText()) {

                        case "Seguridad":
                                try {
                            usuario = txt_usuario.getText();
                            MDI_Sistema menuGeneral = new MDI_Sistema();
                            menuGeneral.setVisible(true);
                            this.dispose();
                        } catch (Exception e) {
                            System.out.println(e);
                        }
                        break;

                        case "Hoteleria":
                                try {
                            usuario = txt_usuario.getText();
                            UIManager.setLookAndFeel(new FlatLightLaf());
                            Hoteleria_MDI menuHoteleria = new Hoteleria_MDI();
                            menuHoteleria.setVisible(true);
                            this.dispose();
                        } catch (Exception e) {
                            System.out.println(e);
                        }
                        break;
                    }
            }
        }
    }//GEN-LAST:event_btn_iniciar_sesionMouseClicked

