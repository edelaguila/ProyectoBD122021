    private void Btn_eliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_eliminarMouseClicked
        if (prcs_repetidos.isNoneEmpty(Txt_codigo)) {
            if (prcs_repetidos.isNumeric(Txt_codigo.getText())) {
                if (prcs_repetidos.ConfirmarEliminacion("eliminar", "Viaje", this)) {
                    ViajeDAO serviciosdao = new ViajeDAO();
                    servicios.setId(Txt_codigo.getText());
                    serviciosdao.delete(servicios);
                    actualizarTabla("");
                    prcs_repetidos.AlertaMensaje("eliminada", "Viaje", "exitosamente");
                    bitacora.GuardarEnBitacora("Eliminado", "2222");
                    Limpiar();
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo eliminar el Viaje");
                }
            }
        }
    }//GEN-LAST:event_Btn_eliminarMouseClicked

