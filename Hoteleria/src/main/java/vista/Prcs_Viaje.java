    private void Btn_guardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_guardarMouseClicked
        if (prcs_repetidos.isNoneEmpty(Txt_codigo, Txt_precio, Txt_destino) && Habitacion.getSelectedItem().toString().length() != 0 && Transporte.getSelectedItem().toString().length() != 0) {
            if (prcs_repetidos.isNumeric(Txt_codigo.getText(), Txt_precio.getText())) {
                ViajeDAO serviciosdao = new ViajeDAO();
                servicios.setId(Txt_codigo.getText());
                servicios.setHabitacion(Habitacion.getSelectedItem().toString());
                servicios.setTransporte(Transporte.getSelectedItem().toString());
                servicios.setDestino(Txt_destino.getText());
                servicios.setPrecio(Txt_precio.getText());
                serviciosdao.insert(servicios);
                actualizarTabla("");
                prcs_repetidos.AlertaMensaje("guardado", "Viaje", "exitosamente");
                bitacora.GuardarEnBitacora("Guardado", "2222");
                Limpiar();
            }
        }
    }//GEN-LAST:event_Btn_guardarMouseClicked

