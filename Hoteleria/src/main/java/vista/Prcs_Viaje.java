    public void actualizarTabla(String codigo) {
        ProcesosRepetidos prcs_repetidos = new ProcesosRepetidos();
        ViajeDAO.codigoAuxiliar = codigo;
        ViajeDAO.nombreAuxiliar = codigo;
        ViajeDAO.apellidoAuxiliar = codigo;
        String columnas[] = {"ID", "Habitacion", "Transporte", "Destino", "Precio"};
        int cantidadcolumnas = columnas.length;
        prcs_repetidos.llenarColumnas(columnas, cantidadcolumnas, Tbl_Datos);
        String datos[] = new String[cantidadcolumnas];
        int tamaño[] = {100, 100, 200, 250, 100};
        ViajeDAO serviciosdao = new ViajeDAO();
        List<Viaje> servicio = serviciosdao.select();
        for (Viaje listaServicio : servicio) {
            datos[0] = listaServicio.getId();
            datos[1] = listaServicio.getHabitacion();
            datos[2] = listaServicio.getTransporte();
            datos[4] = listaServicio.getDestino();
            datos[3] = listaServicio.getPrecio();

            prcs_repetidos.llenarFilas(datos, tamaño, Tbl_Datos);
        }
    }

    public void Limpiar() {
        prcs_repetidos.Limpiar(Txt_codigo, Txt_destino, Txt_buscar, Txt_precio);
        Habitacion.setSelectedItem("Seleccionar...");
        Transporte.setSelectedItem("Seleccionar...");
    }

    public void cargar_Habitaciones() {
        Habitacion.addItem("Seleccionar...");
        HabitacionDAO.codigoAuxiliar = "";
        HabitacionDAO pisosdao = new HabitacionDAO();
        List<Habitacion> piso = pisosdao.select();
        for (Habitacion listaPiso : piso) {
            Habitacion.addItem(String.valueOf(listaPiso.getId()));
        }
    }

    public void cargar_Transporte() {
        Transporte.addItem("Seleccionar...");
        TransporteDAO.codigoAuxiliar = "";
        TransporteDAO pisosdao = new TransporteDAO();
        List<Transporte> piso = pisosdao.select();
        for (Transporte listaPiso : piso) {
            Transporte.addItem(String.valueOf(listaPiso.getId()));
        }
    }

