public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Direcci√≥n");
        modelo.addColumn("Telefono");
        modelo.addColumn("Nit");
        modelo.addColumn("Email");
        modelo.addColumn("Saldo");
        modelo.addColumn("Estado");
        ProveedorDAO proveedorDAO = new ProveedorDAO();
        
        List<Proveedor> proveedor = proveedorDAO.select();
        Tbl_Datos.setModel(modelo);
        String[] dato = new String[8];
        for (int i = 0; i < proveedor.size(); i++) {
            dato[0] = proveedor.get(i).getPK_codigo_Proveedor();
            dato[1] = proveedor.get(i).getNombre_Proveedor();
            dato[2] = proveedor.get(i).getDireccion_Proveedor();
            dato[3] = proveedor.get(i).getTelefono_Proveedor();
            dato[4] = proveedor.get(i).getNit_Proveedor();
            dato[5] = proveedor.get(i).getEmail_Proveedor();
            dato[6] = proveedor.get(i).getSaldo_Proveedor();
            dato[7] = proveedor.get(i).getEstatus_Proveedor();
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }
