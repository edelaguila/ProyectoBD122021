    public void llenadoDeTablas() {
        /**
         *
         * creaccion de la tabla de de titulos
         */
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codico Cliente");
        modelo.addColumn("Nombre Cliente");
        modelo.addColumn("Direccion Cliente");
        modelo.addColumn("telefono Cliente");

        modelo.addColumn("Nit Cliente");
        modelo.addColumn("Email Cliente");
           modelo.addColumn("Saldo Cliente");
              modelo.addColumn("Cuenta Cliente");


        modelo.addColumn("Estatus Cliente");
      

        ClienteDao ventasDAO = new ClienteDao();

        List<Cliente> ventas = ventasDAO.select();
        JtProductos1.setModel(modelo);
        String[] dato = new String[9];
        for (int i = 0; i < ventas.size(); i++) {
            dato[0] = (ventas.get(i).getCodigo_Cliente());
            dato[1] = ventas.get(i).getNombre_Cliente();
             dato[2] = ventas.get(i).getDireccion_Cliente();
             dato[3] = (ventas.get(i).getTelefono_Cliente());
            dato[4] = ventas.get(i).getNit_Cliente();
            dato[5] = (ventas.get(i).getEmail_Cliente());
              dato[6] = (ventas.get(i).getSaldo_Cliente());
                dato[7] = (ventas.get(i).getCuenta_Cliente());
            dato[8] = (ventas.get(i).getEstatus_Cliente());
         

            System.out.println("vendedor:" + ventas);
            modelo.addRow(dato);
        }
    }

